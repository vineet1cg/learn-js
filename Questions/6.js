var temp_celcius = 32;
if(temp_celcius<0){
    console.log("Freezing Cold");
}
else if(temp_celcius>=0 && temp_celcius<=15){
console.log("Very Cold");
}
else if(temp_celcius>=16 && temp_celcius<=25){
console.log("Cold");
}
else if(temp_celcius>=26 && temp_celcius<=35){
console.log("Warm");
}
else if(temp_celcius>=36){
console.log("Hot");
}